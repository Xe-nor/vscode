# Initialize two variables
a <- 10
b <- 20

# Calculate the sum of the two variables
sum <- a + b
print(paste("The sum of", a, "and", b, "is", sum))

# Calculate the multiplication of the two variables
product <- a * b
print(paste("The product of", a, "and", b, "is", product))

# Calculate the difference between the two variables
difference <- a - b
print(paste("The difference between", a, "and", b, "is", difference))

# Calculate the division of the two variables
quotient <- a / b
print(paste("The quotient of", a, "divided by", b, "is", quotient))
